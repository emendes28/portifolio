    private void salvarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarBtnActionPerformed
        Aluno aluno = new Aluno();
        Date data = new Date(System.currentTimeMillis()); 
        if(matriculaTxt != null && nomeTxt != null && enderecoTxt != null && emailTxt != null){
            aluno.setDtCadastro(data);
            aluno.setEmail(emailTxt.getText());
            aluno.setEndereco(enderecoTxt.getText());
            aluno.setNome(nomeTxt.getText());
            aluno.setNumeroMatricula(Integer.parseInt(matriculaTxt.getText()));
            bd.addAluno(aluno);               
        }
        new CadastroAluno().setVisible(false);
       ListarAlunos principal = new ListarAlunos();
       principal.setVisible(true);
       principal.atualizaTabela(bd.buscar());
        
    }//GEN-LAST:event_salvarBtnActionPerformed

